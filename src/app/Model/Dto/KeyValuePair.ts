/**
 * 参数
 * 
 * @export
 * @class KeyValuePair
 */
export class KeyValuePair {
    constructor() { }
    /**
     * 主键
     * 
     * @type {*}
     * @memberof KeyValuePair
     */
    Key:any;
    /**
     * 值
     * 
     * @type {*}
     * @memberof KeyValuePair
     */
    Value:any;

    /**
     * 类型
     * 
     * @type {string}
     * @memberof KeyValuePair
     */
    Type:string;
}